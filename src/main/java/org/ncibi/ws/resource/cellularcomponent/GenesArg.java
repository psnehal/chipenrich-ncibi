package org.ncibi.ws.resource.cellularcomponent;

import java.util.LinkedList;
import java.util.List;

public class GenesArg
{
    private final List<Integer> geneids = new LinkedList<Integer>();
    
    public void addGeneid(int geneid)
    {
        geneids.add(geneid);
    }
    
    public List<Integer> getGeneids()
    {
        return geneids;
    }
}
